/* PRIMER EJERCICIO
Hacer una suma, resta, división y multiplicación, 
cada una se guarda en una variable diferente, y 
se debe mostrar en consola */

public class Ejercicio1 {

    public static void main (String args []) {

        //Suma
        int suma = 3 + 27;
        System.out.println("Resultado de suma: " + suma);

        // Resta
        int resta = 64 - 43;
        System.out.println("Resultado de resta: " + resta);

        // Division 
        double division = 79.0 / 6 ;
        System.out.println("Resultado de division: " + division);

        // Multiplicación
        int multiplicacion = 31 * 60;
        System.out.println("Resultado de multiplicacion: " + multiplicacion);


    }
}