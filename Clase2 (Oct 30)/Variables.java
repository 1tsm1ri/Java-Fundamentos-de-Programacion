// CLASE DE VARIABLES
public class Variables {

    // Metodo main, inicio de ejecucion del codigo
    public static void main(String args []) {
    

        //TIPOS DE DATOS (con su valor max)
        long vLong = Long.MAX_VALUE; // se debe de añadir una "L" al final
        int vInt = Integer.MAX_VALUE;
        short vShort = Short.MAX_VALUE;
        byte vByte = Byte.MAX_VALUE;
        double vDouble = Double.MAX_VALUE;
        float vFloat = Float.MAX_VALUE; // se debe de añadir una "f" al final
        boolean vBoolean = true;
        char vChar = 'M';

        System.out.println(vLong);
        System.out.println(vInt);
        System.out.println(vShort);
        System.out.println(vByte);
        System.out.println(vDouble);
        System.out.println(vFloat);
        System.out.println(vBoolean);
        System.out.println(vChar);
    

    }
}